import { promises as fs } from 'node:fs';
import path from 'node:path';
import { fileURLToPath } from 'node:url';
import { parse as parseYaml } from 'yaml';

const __filename = fileURLToPath(import.meta.url);
const __dirname = path.dirname(__filename);
const repoRoot = path.resolve(__dirname, '..', '..');
const promptsDir = path.join(repoRoot, 'prompts');
const promptDefinitionExtensions = new Set(['.json', '.yaml', '.yml']);

function codeFence(language, content) {
  return `\`\`\`${language}\n${content}\n\`\`\``;
}

function blockquote(text) {
  return text
    .split('\n')
    .map((line) => `> ${line}`)
    .join('\n');
}

export async function loadPromptDefinitions(directory = promptsDir) {
  const entries = await fs.readdir(directory, { withFileTypes: true });
  const definitions = [];

  for (const entry of entries) {
    if (!entry.isFile()) {
      continue;
    }

    const filePath = path.join(directory, entry.name);
    const extension = path.extname(filePath).toLowerCase();

    if (!promptDefinitionExtensions.has(extension)) {
      continue;
    }

    const raw = await fs.readFile(filePath, 'utf8');
    const definition =
      extension === '.json'
        ? JSON.parse(raw)
        : parseYaml(raw);

    if (!definition || typeof definition !== 'object' || Array.isArray(definition)) {
      throw new Error(`Prompt definition ${filePath} did not parse to an object.`);
    }

    definitions.push({
      ...definition,
      sourcePath:
        definition.sourcePath ??
        path.relative(repoRoot, filePath).split(path.sep).join('/'),
    });
  }

  return definitions.sort((a, b) => a.id.localeCompare(b.id));
}

export function buildFullPrompt(definition) {
  return definition.promptSections.map((section) => section.content).join('\n\n');
}

export function renderPromptDocs(definition) {
  const fullPrompt = buildFullPrompt(definition);
  const designPrinciples = definition.designPrinciples
    .map((principle, index) => `${index + 1}. **${principle.title}** ${principle.body}`)
    .join('\n\n');

  const promptSections = definition.promptSections
    .map(
      (section) =>
        `### ${section.heading}\n\n${codeFence(section.language, section.content)}\n\n${blockquote(`**${section.commentaryTitle}:** ${section.commentary}`)}`
    )
    .join('\n\n');

  const iterationRows = definition.iterationLog
    .map((entry) => `| ${entry.date} | ${entry.change} | ${entry.reason} |`)
    .join('\n');

  return `# ${definition.title}

> This file is generated from \`${definition.sourcePath}\`.
> Runtime prompt file: \`${definition.runtimePath}\`.
> Do not edit this Markdown directly. Run \`npm run prompts:generate\`.

---

## Purpose

${definition.purpose.join('\n\n')}

---

## Design Principles

${designPrinciples}

---

## The Prompt

${promptSections}

---

## Full Prompt (Copy-Paste Ready)

For the runtime \`${path.basename(definition.runtimePath)}\` file, here is the complete prompt as a single string:

${codeFence(
  'typescript',
  `export const ${definition.runtimeExportName} = \`${fullPrompt}\`;`
)}

---

## Context Packet (User Message)

${definition.contextPacket.description}

${codeFence('text', definition.contextPacket.template)}

${blockquote(`**${definition.contextPacket.commentaryTitle}:** ${definition.contextPacket.commentary}`)}

---

## Iteration Log

| Date | Change | Reason |
|------|--------|--------|
${iterationRows}
`;
}

export function renderRuntimePrompt(definition) {
  const fullPrompt = buildFullPrompt(definition);

  return `/**
 * Generated from \`${definition.sourcePath}\`.
 * Do not edit this file directly; run \`npm run prompts:generate\`.
 */

export const ${definition.runtimeExportName} = \`${fullPrompt}\`;
`;
}

async function writeFileIfChanged(targetPath, content) {
  let current = null;

  try {
    current = await fs.readFile(targetPath, 'utf8');
  } catch (error) {
    if (error.code !== 'ENOENT') {
      throw error;
    }
  }

  if (current === content) {
    return false;
  }

  await fs.mkdir(path.dirname(targetPath), { recursive: true });
  await fs.writeFile(targetPath, content, 'utf8');
  return true;
}

async function readFileIfPresent(targetPath) {
  try {
    return await fs.readFile(targetPath, 'utf8');
  } catch (error) {
    if (error.code === 'ENOENT') {
      return null;
    }
    throw error;
  }
}

export async function generatePromptArtifacts() {
  const definitions = await loadPromptDefinitions();
  const results = [];

  for (const definition of definitions) {
    const docsPath = path.join(repoRoot, definition.docsPath);
    const runtimePath = path.join(repoRoot, definition.runtimePath);
    const docsChanged = await writeFileIfChanged(docsPath, renderPromptDocs(definition));
    const runtimeChanged = await writeFileIfChanged(
      runtimePath,
      renderRuntimePrompt(definition)
    );

    results.push({
      id: definition.id,
      docsChanged,
      runtimeChanged,
      docsPath,
      runtimePath,
    });
  }

  return results;
}

export async function checkPromptArtifacts() {
  const definitions = await loadPromptDefinitions();
  const mismatches = [];

  for (const definition of definitions) {
    const expectedDocs = renderPromptDocs(definition);
    const expectedRuntime = renderRuntimePrompt(definition);
    const docsPath = path.join(repoRoot, definition.docsPath);
    const runtimePath = path.join(repoRoot, definition.runtimePath);

    const actualDocs = await readFileIfPresent(docsPath);
    if (actualDocs === null) {
      mismatches.push(`${definition.docsPath} is missing for ${definition.sourcePath}`);
    } else if (actualDocs !== expectedDocs) {
      mismatches.push(`${definition.docsPath} is out of date with ${definition.sourcePath}`);
    }

    const actualRuntime = await readFileIfPresent(runtimePath);
    if (actualRuntime === null) {
      mismatches.push(`${definition.runtimePath} is missing for ${definition.sourcePath}`);
    } else if (actualRuntime !== expectedRuntime) {
      mismatches.push(`${definition.runtimePath} is out of date with ${definition.sourcePath}`);
    }
  }

  return mismatches;
}
